package com.alura.reservaciones.utilities;

import java.awt.Color;

public class MyColor {
	private final Color customWhite = new Color(245, 245, 245);
	private final Color customBlack = new Color(12, 12, 12);
	private final Color customBlackBlue = new Color(30, 40, 49);
	private final Color customBlue = new Color(12, 138, 199);
	private final Color separatorColor = new Color(0, 120, 215);
	private final Color hoverBlue = new Color(118, 187, 223);
	
	
	public Color getCustomWhite() {
		return customWhite;
	}
	public Color getCustomBlack() {
		return customBlack;
	}
	
	public Color getCustomBlackBlue() {
		return customBlackBlue;
	}
	
	public Color getCustomBlue() {
		return customBlue;
	}
	
	public Color getSeparatorColor() {
		return separatorColor;
	}
	public Color getHoverBlue() {
		return hoverBlue;
	}
	
	
	
	
}
